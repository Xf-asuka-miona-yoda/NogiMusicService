public class Mymessage {
    private String type;
    private String objectid; //对象id
    private String objectcontent;//对象内容
    private String objecttime; //对象时间
    private String userid;
    private String username;
    private String content;
    private String time;

    public String getObjectcontent() {
        return objectcontent;
    }

    public void setObjectcontent(String objectcontent) {
        this.objectcontent = objectcontent;
    }

    public String getObjecttime() {
        return objecttime;
    }

    public void setObjecttime(String objecttime) {
        this.objecttime = objecttime;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getObjectid() {
        return objectid;
    }

    public void setObjectid(String objectid) {
        this.objectid = objectid;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
